          <h6 style="text-align: justify; margin-left: 6%; margin-right: 6%;">
          Les postes afférents peuvent être :<strong>  DIRECTEUR ARTISTIQUE, WEBMESTRE, GRAPHISTE D’EDITION, GRAPHISTE MULTIMEDIA, MAQUETTISTE, INFOGRAPHISTE, WEBDESIGNER, DESIGNER GRAPHISTE,  PRODUCTEUR DE JEUX, CHEF DE PROJETS PRESTATAIRE, …</strong> 

          Nous avons examiné pour vous quelques uns afin de mieux vous situer.
          
           
          
        <strong> DIRECTEUR ARTISTIQUE :</strong>  Le directeur artistique est le responsable de l’aspect visuel et artistique d’une publication ou d’un projet multimédia, d’une campagne de publicité, d’une production cinématographique  ou audiovisuelle. Avec l’aide de son équipe de création, il traduit les messages  par l’image, invente des logos, élabore des maquettes de journaux, conçoit des emballages… Son objectif majeur : le style et la personnalité. <br><br>
          
      <strong> WEBMESTRE :</strong>  À la fois technicien, graphiste et rédacteur, le webmestre/webmaster est responsable de la vie d’un site, du développement à l’animation, en passant par la mise en ligne et la veille technologique.
          
          <br><br>
          
        <strong> GRAPHISTE D’EDITION :</strong>  Le graphisme d’édition est celui qui comprend les livres et les recueils : scolaires, de cuisine, de poésie, de référence, dictionnaires… Le domaine de l’édition permet parfois au graphiste d’exprimer tout son talent dans un contexte peu contraignant (ex : livres d’art). Plus fréquemment, la créativité se limitera à la jaquette du livre (ex : romans). Le graphisme d’édition regroupe aussi tous les périodiques : journaux, bulletins (municipaux, d’entreprises, d’associations), revues, magazines… Ce type de projet implique une vision à long terme, une bonne planification, une grande minutie et un sens de l’organisation. Qu’il s’agisse d’un journal ou d’un magazine, les périodiques ont un public cible, un style d’information et une philosophie qui leur est propre. Ils doivent conserver cette personnalité d’une parution à l’autre. Le visuel doit donc être constant et cohérent.
          
          <br><br>
          
        <strong> GRAPHISTE MULTIMEDIA :</strong>  le travail du graphiste multimédia se situe à mi-chemin entre celui du maquettiste et du dessinateur. Son rôle est de créer des visuels, des animations ou des graphiques à destination de divers supports multimédias. Pour ce faire, il utilise des outils de programmation et des logiciels spécifiques tels que Photoshop ou Adobe Illustrator. Mais le graphiste multimédia ne se contente pas de créer ; il réalise aussi un suivi du travail effectué, et mesure l’impact de son idée. Il doit également assurer une veille technologique régulière afin de pouvoir s’adapter aux nouvelles contraintes de son métier, et permettre ainsi à son entreprise de rivaliser avec la concurrence.
          
          <br><br>
          
        <strong> MAQUETTISTE :</strong>  Le métier de maquettiste consiste à assurer la mise en page d’un support visuel : la police des caractères, la répartition des textes et des images, l’emplacement des blancs…Le maquettiste peut avoir à réaliser la maquette d’un magazine, d’une brochure, d’un livre ou encore d’un site web. Inventif, le maquettiste doit pouvoir créer une mise en page agréable à l’œil qui facilite la lecture et le repérage des éléments textuels et visuels.
          <br><br>
           
          
        <strong> INFOGRAPHISTE :</strong>  L’infographiste a pour mission de réaliser des images et des graphismes en deux ou trois dimensions pour un site internet ou une édition papier de supports de communication (affiches, flyers, logos, images, animations, etc.). L’infographiste peut-être spécialisé dans la 2D (supports de communications, sites internet) ou bien dans la 3D (jeux, animation). Il s’agit d’un métier de passion demandant une formation solide en arts. 
          
           <br><br>
          
          <strong>WEBDESIGNER :</strong>  Le (la) webdesigner réalise le design des sites web, mais aussi des mobiles, des applications, des plateformes digitales. Son rôle est de créer une interface destinée à l’utilisateur. Cette interface aura plusieurs objectifs : répondre aux fonctionnalités du site web, être ergonomique et donner l’esprit du site ou de l’application.
